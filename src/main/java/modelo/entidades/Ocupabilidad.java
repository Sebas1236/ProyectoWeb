package modelo.entidades;

public class Ocupabilidad {
    private int idOcu;
    private String tipoOcu;
    private String descripcionOcu;

    public int getIdOcu() {
        return idOcu;
    }

    public void setIdOcu(int idOcu) {
        this.idOcu = idOcu;
    }

    public String getTipoOcu() {
        return tipoOcu;
    }

    public void setTipoOcu(String tipoOcu) {
        this.tipoOcu = tipoOcu;
    }

    public String getDescripcionOcu() {
        return descripcionOcu;
    }

    public void setDescripcionOcu(String descripcionOcu) {
        this.descripcionOcu = descripcionOcu;
    }
    
}
